  drawScoreTable() {
    const type = state.gameType;
    let name = '';

    if (state.settings.lang === 'en') {
      name = `${type[0].toUpperCase() + type.slice(1)} score`;
    } else if (type === 'artists') {
      name = 'Художники';
    } else {
      name = 'Картины';
    }
    let isEmpty;
    let placeholderScore = create.createScoreTable();
    if (!placeholderScore.length) {
      placeholderScore = 'You haven\'t played blitz yet';
      isEmpty = true;
    }
    this.main.innerHTML = `
        <div class="score-block">
        <h3>${name}</h3>
        <div class="score-block-inner ${isEmpty ? 'empty-score' : ''}">
            ${placeholderScore}
        </div>
        </div>
        `;

    const cards = document.querySelectorAll('.placeholder-card');
    const data = state.scoreStorage[state.gameType];

    cards.forEach((card, i) => {
      card.style.background = `center / cover no-repeat url(https://raw.githubusercontent.com/strawberrystr1/image-data/master/img/${state.gameType === 'blitz' ? data[i].picSet[0].imageNum : data[i].picSet[0].imageNum}.jpg)`;
    });

    this.main.classList.remove('hide');
  }

  drawScoreResults() {
    const type = state.gameType;
    let name = '';

    if (state.settings.lang === 'en') {
      name = `${type[0].toUpperCase() + type.slice(1)} score`;
    } else if (type === 'artists') {
      name = 'Художники';
    } else if (type === 'pictures') {
      name = 'Картины';
    } else {
      name = '';
    }

    const template = create.createScoreResults();
    this.main.innerHTML = `
        <div class="score-block">
        <h3>${name}</h3>
        <div class="score-block-inner">
            ${template}
        </div>
        </div>
        `;

    const cards = document.querySelectorAll('.score-card');
    const data = state.scoreStorage[state.gameType][state.showScoreIndex].picSet;
    cards.forEach((item, i) => {
      item.style.background = `center / cover no-repeat url(https://raw.githubusercontent.com/strawberrystr1/image-data/master/img/${data[i].imageNum}.jpg)`;
      if (!item.classList.contains('correct-card')) {
        item.style.background = `linear-gradient(black, black), center / cover no-repeat url(https://raw.githubusercontent.com/strawberrystr1/image-data/master/img/${data[i].imageNum}.jpg)`;
        item.style.backgroundBlendMode = 'saturation';
      }
    });

    this.main.classList.remove('hide');
  }
